def colors_match(rgb_tuple):
    """Vérifie si la couleur correspond au bleu des jours de cours"""
    if not rgb_tuple:
        return False
    r, g, b = rgb_tuple[0], rgb_tuple[1], rgb_tuple[2]
    # Couleur cible : (166, 202, 240) avec tolérance
    return abs(r - 166) < 20 and abs(g - 202) < 20 and abs(b - 240) < 20


def parse_planning_classe_xls(filepath):
    """Lit un fichier .xls avec xlrd (sans libreoffice)"""
    try:
        wb = xlrd.open_workbook(filepath, formatting_info=True)
    except Exception as e:
        return {'nom': Path(filepath).stem, 'jours': [], 'error': str(e)}

    ws = wb.sheet_by_index(0)

    # Nom de la classe (ligne 0)
    nom_classe = None
    for col in range(ws.ncols):
        v = ws.cell_value(0, col)
        if isinstance(v, str) and len(v.strip()) > 3:
            nom_classe = v.strip()
            break

    jours_cours = []

    # Détecter les blocs de mois (ligne 4 = index 4)
    month_blocks = []
    for col in range(ws.ncols):
        v = ws.cell_value(4, col)
        if isinstance(v, str):
            v_low = v.strip().lower()
            for m_name, m_num in MOIS_ABBR.items():
                if m_name in v_low:
                    yr = re.search(r'(20\d\d)', v_low)
                    year = int(yr.group(1)) if yr else None
                    month_blocks.append({
                        'col_jour': col + 1,
                        'col_date': col + 2,
                        'year': year,
                        'month': m_num
                    })
                    break

    for block in month_blocks:
        if not block['year']:
            continue
        cj = block['col_jour']
        cd = block['col_date']
        yr = block['year']
        mn = block['month']

        for row in range(5, ws.nrows):
            if cj >= ws.ncols or cd >= ws.ncols:
                continue

            # Vérifier la couleur de fond de la cellule jour
            try:
                xf_idx = ws.cell_xf_index(row, cj)
                xf = wb.xf_list[xf_idx]
                bg_color_idx = xf.background.pattern_colour_index
                rgb = wb.colour_map.get(bg_color_idx)
                is_cours = colors_match(rgb)
            except:
                is_cours = False

            if not is_cours:
                continue

            # Extraire le numéro de jour
            date_val = ws.cell_value(row, cd)
            day_num = None
            if isinstance(date_val, float) and date_val > 0:
                day_num = int(date_val)
            elif isinstance(date_val, str) and date_val.strip().isdigit():
                day_num = int(date_val.strip())

            if not day_num or not (1 <= day_num <= 31):
                continue

            try:
                d = datetime.date(yr, mn, day_num)
                if d.weekday() < 5:
                    jours_cours.append(d.strftime('%Y-%m-%d'))
            except ValueError:
                pass

    return {'nom': nom_classe or Path(filepath).stem, 'jours': sorted(set(jours_cours))}


